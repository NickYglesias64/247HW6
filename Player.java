public class Player {

    /**
     * Abstractly holds player
     */
    public Player() {}

    /**
     * Outputs all Strings when the commands are run
     */
    public void player() {
        System.out.println("Our hero is born");
    }
    public void jump() {
        System.out.println("our hero jumps over a tall building");
    }
    public void fire() {
        System.out.println("our hero squirts his water gun far into the sky");
    }
    public void runForward() {
        System.out.println("our hero sprints faster than al the cars");
    }
}
